// Exercicio 03 - Soma Simples
// Crie um programa que some dois números inteiros definidos no código e exiba o resultado.

package com.raissasf.semana01fundamentos.basico;

public class SomaSimples {

    public static void main(String[] args) {

        int numero01 = 100;
        int numero02 = 400;

        int soma = numero01 + numero02;

        System.out.println("A soma de " + numero01 + " + " + numero02 + " é igual a: " + soma);
    }
}